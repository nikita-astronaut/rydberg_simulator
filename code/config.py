    def interaction_from_edges(self):
        interaction = np.zeros((self.N_sites, self.N_sites))
        for edges, coupling in zip(self.edges, self.couplings):
            for edge in edges:
                interaction[edge[0], edge[1]] += coupling


        int_symm = (interaction + interaction.T)
        interaction_list = []
        for i in range(self.N_sites):
            cur_list = []

            for j in np.where(int_symm[i] != 0)[0]:
                cur_list.append((j, int_symm[i, j]))

            interaction_list.append(deepcopy(cur_list))

        return interaction, interaction_list


